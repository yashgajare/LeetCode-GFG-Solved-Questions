class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &prices) {
        // code here
        bool res = is_sorted(prices.begin(),prices.end());
        int mn=INT_MAX, mx=0;
        
        if(!res){
            for(int i=0;i<prices.size()-1;i++){
                if(prices[i]<prices[i+1]){
                    mx=mx+(prices[i+1]-prices[i]);
                }
            }
            return mx;
        }
        else{
            mn=prices[prices.size()-1]-prices[0];
            return mn;
        }
    }
};


